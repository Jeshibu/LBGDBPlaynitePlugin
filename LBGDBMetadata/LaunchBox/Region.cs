        private static readonly Regex NoIntroTags = new Regex(@"(?<=\()([\w\s]+,?)+(?=\))", RegexOptions.IgnoreCase | RegexOptions.Compiled);

        public static string GetRegionNoIntro(this string text)
        {
            var tagGroups = NoIntroTags.Matches(text);
            foreach (Match tagGroup in tagGroups)
            {
                var regionList = tagGroup.Value.Split(',').Select(region => region.Sanitize())
                    .Where(region => !string.IsNullOrWhiteSpace(region)).Distinct().ToList();

                if (regionList.All(region => RegionList.ContainsKey(region)))
                {
                    return string.Join(",",regionList);
                }
            }

            return "";
        }

        public static List<string> GetRegions(this string region)
        {
            List<string> regionList = new List<string>();
            if (!string.IsNullOrWhiteSpace(region))
            {
                regionList = region.Split(',').Select(item => item.Sanitize()).Where(item => !string.IsNullOrWhiteSpace(item)).Distinct().ToList();
            }

            return regionList;
        }

        public static Dictionary<string, int> GetRegionPriorityList(this Playnite.SDK.Models.Game game)
        {
            return game.Regions.Select(r => r.Name).GetRegionPriorityList();
        }

        public static Dictionary<string, int> GetRegionPriorityList(this string region)
        {
            return new[] { region }.GetRegionPriorityList();
        }

        public static Dictionary<string, int> GetRegionPriorityList(this IEnumerable<string> regions)
        {
            Dictionary<string, int> regionPriorityList = new Dictionary<string, int>();
            var rl = regions?.Distinct().ToList();
            if (rl == null || rl.Count == 0)
            {
                GetRegionPriority("canada", regionPriorityList);
                GetRegionPriority("usa", regionPriorityList);
            }
            else
            {
                foreach (var r in regions)
                {
                    GetRegionPriority(r, regionPriorityList);
                }
            }
            
            return regionPriorityList;
        }
        
        private static void GetRegionPriority(string region, Dictionary<string, int> regionPriorityList)
        {
            if (RegionList.TryGetValue(region, out var regionInfo))
            {
                if (regionPriorityList.ContainsKey(regionInfo.RegionName))
                {
                    return;
                }

                regionPriorityList.Add(regionInfo.RegionName, regionPriorityList.Count);
                if (regionInfo.ParentRegion != null)
                {
                    GetRegionPriority(regionInfo.ParentRegion, regionPriorityList);
                }
            }
            else
            {
                if (!regionPriorityList.ContainsKey(region))
                {
                    regionPriorityList.Add(region, regionPriorityList.Count);
                }
                if (!regionPriorityList.ContainsKey(None))
                {
                    regionPriorityList.Add(None, regionPriorityList.Count);
                }
            }
        }



